function nd_ρ(α)
    1.6612*α - 0.4721*α^2 + 0.0671*α^3 - 0.0043*α^4 + 0.000106*α^5
end

function br_β(α)
    0.7858 - 1.2344*α + 0.7949*α^2 - 0.1238*α^3 + 0.0064*α^4
end

function ucvm1d_ρ(α)
    1.865 + 0.1579*α
end

function ucvm1d_β(α, ρ)
    if ρ < 2.06
        ν = 0.4
    elseif ρ > 2.5
        ν = 0.25
    else
        ν = 0.40 - ((ρ - 2.06) * 0.15 / 0.44)
    end

    α*sqrt((0.5 - ν)/(1.0 - ν))
end
